                                    <h4>Services Informatiques aux Organisations (SIO)</h4>
                                    <p>Le BTS Services Informatiques aux Organisations est un diplôme reconnu par l'état de niveau Bac+2,
                                        il remplace depuis septembre 2011 l'ancien BTS Informatique de Gestion (BTS IG).<br><br>
                                        En effet, cette nouvelle formule répond aux attentes de la profession en matière de qualification
                                        et propose deux spécialités bien distinctes : le BTS SIO SISR et le BTS SIO SLAM.<br><br>
                                        En plus des matières en lien avec l'ingénieurie informatique,
                                        la formation propose également des enseignements de culture générale,
                                        d’anglais, de mathématiques appliquées à l’informatique,
                                        ainsi que sur les outils d’analyse économique, managériale et juridique des services informatiques.
                                    </p>
                                </div>
                            </div>
                        </div>
                    </div>
                    <div class="col-lg-6 col-xs-12">
                        <div class="container">
                            <div class="box-item">
                                <div class="text">
                                    <h4>Solutions logicielles et applications métiers (SLAM)</h4>
                                    <p>L'option SLAM vise à permettre de suivre l'ensemble du cycle de vie des applications :
                                        analyser les besoins des utilisateurs finaux, rechercher et choisir une solution adaptée,
                                        concevoir et développer des applications (génie logiciel),
                                        assurer la maintenance des programmes qui garantissent le bon fonctionnement de ces applications.</p>
                                </div>
                                <div class="text">
                                    <h4>Solutions d’Infrastructure Système et Réseaux (SISR)</h4>
                                    <p>Cette option du BTS SIO forme des techniciens supérieurs capables de gérer un parc
                                        informatique ou d’administrer un réseau en PME.
                                        Leur mission consiste alors à automatiser et rationaliser les activités administratives de l’entreprise.</p>
                                </div>
